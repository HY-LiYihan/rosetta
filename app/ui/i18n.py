def init_language() -> str:
    if LANGUAGE_KEY not in st.session_state:
        st.session_state[LANGUAGE_KEY] = DEFAULT_LANGUAGE
    return str(st.session_state[LANGUAGE_KEY])


def set_language(language: str) -> None:
    st.session_state[LANGUAGE_KEY] = language if language in LANGUAGES else DEFAULT_LANGUAGE


def get_language() -> str:
    return init_language()


def get_text(language: str, key: str, **kwargs: Any) -> str:
    template = TEXT.get(language, TEXT[DEFAULT_LANGUAGE]).get(key)
    if template is None:
        template = TEXT[DEFAULT_LANGUAGE].get(key, key)
    return template.format(**kwargs) if kwargs else template


def t(key: str, **kwargs: Any) -> str:
    return get_text(get_language(), key, **kwargs)
